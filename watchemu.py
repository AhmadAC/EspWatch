    def load_workspace(self, folder):
        self.lbl_folder_path.setText(os.path.basename(folder))
        self.log_info(f"Scanning project directory: '{folder}'")
        
        main_c_path = None
        clock_path = None
        gear_path = None
        
        for root, dirs, files in os.walk(folder):
            for file in files:
                full_path = os.path.join(root, file)
                if file.lower() == "main.c":
                    main_c_path = full_path
                elif file.lower() == "clock.c":
                    clock_path = full_path
                elif file.lower() == "gear.c":
                    gear_path = full_path

        # 1. Parse Image Assets
        if clock_path:
            self.log_info(f"Parsing clock asset: {os.path.basename(clock_path)}")
            self.pix_clock = LVGLImageParser.parse_file(clock_path)
        else:
            self.log_warn("Could not find 'clock.c' in selected folder.")
            if hasattr(self, 'pix_clock'): delattr(self, 'pix_clock')

        if gear_path:
            self.log_info(f"Parsing settings gear asset: {os.path.basename(gear_path)}")
            self.pix_gear = LVGLImageParser.parse_file(gear_path)
        else:
            self.log_warn("Could not find 'gear.c' in selected folder.")
            if hasattr(self, 'pix_gear'): delattr(self, 'pix_gear')

        # 2. Parse main.c coordinates, layouts, and style scales
        if main_c_path:
            self.log_info(f"Parsing main.c configuration: {os.path.basename(main_c_path)}")
            self.parsed_config = MainCParser.parse_main_c(main_c_path)
        else:
            self.log_warn("Could not find 'main.c' in folder. Falling back to default layout.")
            self.parsed_config = {"tiles": {}, "buttons": [], "labels": []}

        # 4. Dynamic layouts and label reconstructions
        self.rebuild_dynamic_labels()
        self.rebuild_dynamic_launcher()

        # 5. Save to config file (Persist folder choice)
        self.save_workspace_path(folder)

