# 2. DYNAMIC MAIN.C FILE PARSER
# ==============================================================================
class MainCParser:
    """
    Parses watch main.c files dynamically to extract active display layouts,
    aligned coordinates, tile counts, and interactive click event routing.
    """
    @staticmethod
    def parse_main_c(filepath):
        config = {
            "tiles": {},          # var_name -> index (e.g. 'tile_clock' -> 0)
            "buttons": [],        # list of dicts with coordinate parameters
            "labels": [],         # list of dynamically parsed labels
            "callbacks": {},      # func_name -> target_tile_var (e.g. 'btn_open_clock_cb' -> 'tile_clock')
            "screen_width": 410,
            "screen_height": 502,
        }
        
        if not filepath or not os.path.exists(filepath):
            return config
            
        try:
            with open(filepath, 'r', encoding='utf-8', errors='ignore') as f:
                content = f.read()

            # Parse physical LCD resolutions
            res_w = re.search(r'#define\s+LCD_H_RES\s+(\d+)', content)
            res_h = re.search(r'#define\s+LCD_V_RES\s+(\d+)', content)
            if res_w: config["screen_width"] = int(res_w.group(1))
            if res_h: config["screen_height"] = int(res_h.group(1))

            # Parse Grid Setup: tile_var = lv_tileview_add_tile(tv, x, y, ...)
            tile_matches = re.finditer(r'(\w+)\s*=\s*lv_tileview_add_tile\(\s*tv\s*,\s*(\d+)\s*,\s*(\d+)\s*', content)
            for m in tile_matches:
                var_name = m.group(1)
                x_idx = int(m.group(2))
                config["tiles"][var_name] = x_idx

            # Parse Click Callbacks: void callback_name(...) { lv_obj_set_tile(tv, target_tile, ...); }
            cb_matches = re.finditer(
                r'void\s+(\w+)\s*\([^)]*\)\s*\{\s*lv_obj_set_tile\(\s*tv\s*,\s*(\w+)\s*,', 
                content
            )
            for m in cb_matches:
                func_name = m.group(1)
                target_tile = m.group(2)
                config["callbacks"][func_name] = target_tile

            # Find all buttons created in launcher: btn_var = lv_button_create(parent_tile_var)
            btn_matches = re.finditer(r'(\w+)\s*=\s*lv_button_create\(\s*(\w+)\s*\)', content)
            buttons_dict = {}
            for m in btn_matches:
                btn_var = m.group(1)
                parent_tile = m.group(2)
                buttons_dict[btn_var] = {
                    "var": btn_var,
                    "parent_tile": parent_tile,
                    "w": 120, "h": 120, # Default layouts
                    "align": "LV_ALIGN_CENTER",
                    "x": 0, "y": 0,
                    "img_src": None,
                    "target_tile_var": None
                }

            # Parse Button Sizes: lv_obj_set_size(btn_var, w, h)
            size_matches = re.finditer(r'lv_obj_set_size\(\s*(\w+)\s*,\s*(\d+)\s*,\s*(\d+)\s*\)', content)
            for m in size_matches:
                btn_var = m.group(1)
                if btn_var in buttons_dict:
                    buttons_dict[btn_var]["w"] = int(m.group(2))
                    buttons_dict[btn_var]["h"] = int(m.group(3))

            # Parse Button Alignment: lv_obj_align(btn_var, ALIGN_TYPE, x, y)
            align_matches = re.finditer(
                r'lv_obj_align\(\s*(\w+)\s*,\s*(\w+)\s*,\s*(-?\d+)\s*,\s*(-?\d+)\s*\)', 
                content
            )
            for m in align_matches:
                btn_var = m.group(1)
                if btn_var in buttons_dict:
                    buttons_dict[btn_var]["align"] = m.group(2)
                    buttons_dict[btn_var]["x"] = int(m.group(3))
                    buttons_dict[btn_var]["y"] = int(m.group(4))

            # Parse Event Registration: lv_obj_add_event_cb(btn_var, callback, ...)
            event_matches = re.finditer(
                r'lv_obj_add_event_cb\(\s*(\w+)\s*,\s*(\w+)\s*,\s*LV_EVENT_CLICKED', 
                content
            )
            for m in event_matches:
                btn_var = m.group(1)
                cb_func = m.group(2)
                if btn_var in buttons_dict and cb_func in config["callbacks"]:
                    buttons_dict[btn_var]["target_tile_var"] = config["callbacks"][cb_func]

            # Parse Image bindings inside button container
            img_creation = re.finditer(r'(\w+)\s*=\s*lv_image_create\(\s*(\w+)\s*\)', content)
            img_to_btn = {}
            for m in img_creation:
                img_to_btn[m.group(1)] = m.group(2)

            img_sources = re.finditer(r'lv_image_set_src\(\s*(\w+)\s*,\s*&?(\w+)\s*\)', content)
            for m in img_sources:
                img_var = m.group(1)
                src_var = m.group(2)
                if img_var in img_to_btn:
                    btn_var = img_to_btn[img_var]
                    if btn_var in buttons_dict:
                        if src_var in ("clock_src", "ui_img_clock"):
                            buttons_dict[btn_var]["img_src"] = "clock"
                        elif src_var in ("gear_src", "ui_img_gear"):
                            buttons_dict[btn_var]["img_src"] = "gear"
                        else:
                            buttons_dict[btn_var]["img_src"] = src_var

            # ==================================================================
            # DYNAMIC LABEL DETECTOR ENGINE
            # ==================================================================
            # Find all label declarations: var_name = lv_label_create(parent)
            lbl_matches = re.finditer(r'(\w+)\s*=\s*lv_label_create\(\s*(\w+)\s*\)', content)
            labels_dict = {}
            for m in lbl_matches:
                lbl_var = m.group(1)
                parent_tile = m.group(2)

                # Default Fallback Layout Metrics
                color = "#ffffff"
                align_type = "LV_ALIGN_CENTER"
                x = 0
                y = 0
                width = "auto"
                font_size = 14

                # 1. Parse Text Colors
                make_m = re.search(r'lv_obj_set_style_text_color\(\s*' + lbl_var + r'\s*,\s*lv_color_make\(\s*(\d+)\s*,\s*(\d+)\s*,\s*(\d+)\s*\)\s*', content)
                if make_m:
                    color = f"rgb({make_m.group(1)},{make_m.group(2)},{make_m.group(3)})"
                else:
                    white_m = re.search(r'lv_obj_set_style_text_color\(\s*' + lbl_var + r'\s*,\s*lv_color_white\(\s*\)\s*', content)
                    if white_m:
                        color = "#ffffff"
                    else:
                        black_m = re.search(r'lv_obj_set_style_text_color\(\s*' + lbl_var + r'\s*,\s*lv_color_black\(\s*\)\s*', content)
                        if black_m:
                            color = "#000000"
                        else:
                            hex_m = re.search(r'lv_obj_set_style_text_color\(\s*' + lbl_var + r'\s*,\s*lv_color_hex\(\s*(0x[0-9a-fA-F]+)\s*\)\s*', content)
                            if hex_m:
                                color = hex_m.group(1).replace("0x", "#")

                # 2. Parse Layout Position Alignment & Offsets
                align_m = re.search(r'lv_obj_align\(\s*' + lbl_var + r'\s*,\s*(\w+)\s*,\s*(-?\d+)\s*,\s*(-?\d+)\s*\)', content)
                if align_m:
                    align_type = align_m.group(1)
                    x = int(align_m.group(2))
                    y = int(align_m.group(3))

                # 3. Parse Dimensions / Target Widths
                width_m = re.search(r'lv_obj_set_width\(\s*' + lbl_var + r'\s*,\s*(\w+)\s*\)', content)
                if width_m:
                    width_val = width_m.group(1)
                    if width_val == "LCD_H_RES":
                        width = 410
                    elif width_val.isdigit():
                        width = int(width_val)

                # 4. Parse Montserrat Font Height Compile Tags or custom arial_160
                font_m = re.search(r'lv_obj_set_style_text_font\(\s*' + lbl_var + r'\s*,\s*&lv_font_montserrat_(\d+)\s*', content)
                if font_m:
                    font_size = int(font_m.group(1))
                else:
                    if re.search(r'lv_obj_set_style_text_font\(\s*' + lbl_var + r'\s*,\s*&arial_160\s*', content):
                        font_size = 160

                labels_dict[lbl_var] = {
                    "var": lbl_var,
                    "parent_tile": parent_tile,
                    "color": color,
                    "align": align_type,
                    "x": x,
                    "y": y,
                    "width": width,
                    "font_size": font_size
                }

            config["buttons"] = list(buttons_dict.values())
            config["labels"] = list(labels_dict.values())
            
        except Exception as e:
            print(f"Error parsing main.c: {e}")
            
        return config


