# 1. UPGRADED ADAPTIVE LVGL C-ARRAY IMAGE ASSET PARSER
# ==============================================================================
class LVGLImageParser:
    """
    Parses LVGL C files generated by SquareLine Studio.
    Supports both LVGL v8 and v9 C structure definitions.
    Automatically detects interleaved vs. separated 3-byte pixel streams.
    """
    @staticmethod
    def parse_file(filepath):
        if not filepath or not os.path.exists(filepath):
            return None

        try:
            with open(filepath, 'r', encoding='utf-8', errors='ignore') as f:
                content = f.read()

            # Parse dimensions using an improved, nested-brace compatible regex
            w_match = re.search(r'\.w\s*=\s*(\d+)|\.header\.w\s*=\s*(\d+)', content)
            h_match = re.search(r'\.h\s*=\s*(\d+)|\.header\.h\s*=\s*(\d+)', content)
            
            width = None
            height = None
            if w_match:
                width = int(next(filter(None, w_match.groups())))
            if h_match:
                height = int(next(filter(None, h_match.groups())))

            # Fallback size deductions if the exporter omitted named initializers
            if width is None or height is None:
                if "clock" in filepath.lower():
                    width, height = 200, 200
                elif "gear" in filepath.lower():
                    width, height = 64, 64
                else:
                    width, height = 120, 120

            # Extract the raw hex array
            array_match = re.search(
                r'const\s+uint8_t\s+ui_img_\w+_(?:data|map)\[\]\s*=\s*\{([^}]+)\}', 
                content
            )
            if not array_match:
                array_match = re.search(
                    r'const\s+LV_ATTRIBUTE_MEM_ALIGN\s+uint8_t\s+\w+_(?:data|map)\[\]\s*=\s*\{([^}]+)\}', 
                    content
                )
            if not array_match:
                array_match = re.search(r'=\s*\{([^}]+)\}', content)

            if not array_match:
                return None

            raw_hex_data = array_match.group(1)
            hex_tokens = re.findall(r'0x[0-9a-fA-F]+', raw_hex_data)
            byte_data = bytes(int(token, 16) for token in hex_tokens)

            # Dynamically calculate bytes per pixel to determine correct layout
            total_pixels = width * height
            bytes_per_pixel = len(byte_data) // total_pixels

            decoded_bytes = bytearray(total_pixels * 4)

            if bytes_per_pixel == 3:
                # Statistical auto-detection for interleaved (v8) vs separate (v9) 3-byte format
                interleaved_alpha_matches = sum(1 for i in range(total_pixels) if byte_data[i * 3 + 2] in (0x00, 0xFF))
                separated_alpha_matches = sum(1 for i in range(total_pixels) if byte_data[total_pixels * 2 + i] in (0x00, 0xFF))
                
                is_interleaved = interleaved_alpha_matches > separated_alpha_matches

                if is_interleaved:
                    # Format: Interleaved RGB565 + Alpha
                    for i in range(total_pixels):
                        base_src = i * 3
                        b0 = byte_data[base_src]
                        b1 = byte_data[base_src + 1]
                        alpha = byte_data[base_src + 2]

                        val = b0 | (b1 << 8)
                        r = ((val >> 11) & 0x1F) * 255 // 31
                        g = ((val >> 5) & 0x3F) * 255 // 63
                        b = (val & 0x1F) * 255 // 31

                        base_dst = i * 4
                        decoded_bytes[base_dst] = b      # Blue
                        decoded_bytes[base_dst + 1] = g  # Green
                        decoded_bytes[base_dst + 2] = r  # Red
                        decoded_bytes[base_dst + 3] = alpha
                else:
                    # Format: RGB565 + 8-bit Alpha (Separate color and alpha arrays)
                    color_len = total_pixels * 2
                    for i in range(total_pixels):
                        b0 = byte_data[i * 2]
                        b1 = byte_data[i * 2 + 1]
                        alpha = byte_data[color_len + i]

                        val = b0 | (b1 << 8)
                        r = ((val >> 11) & 0x1F) * 255 // 31
                        g = ((val >> 5) & 0x3F) * 255 // 63
                        b = (val & 0x1F) * 255 // 31

                        base_dst = i * 4
                        decoded_bytes[base_dst] = b
                        decoded_bytes[base_dst + 1] = g
                        decoded_bytes[base_dst + 2] = r
                        decoded_bytes[base_dst + 3] = alpha

            elif bytes_per_pixel == 2:
                # Standard RGB565 (2 bytes, No Alpha)
                for i in range(total_pixels):
                    base_src = i * 2
                    b0 = byte_data[base_src]
                    b1 = byte_data[base_src + 1]

                    val = b0 | (b1 << 8)
                    r = ((val >> 11) & 0x1F) * 255 // 31
                    g = ((val >> 5) & 0x3F) * 255 // 63
                    b = (val & 0x1F) * 255 // 31

                    base_dst = i * 4
                    decoded_bytes[base_dst] = b
                    decoded_bytes[base_dst + 1] = g
                    decoded_bytes[base_dst + 2] = r
                    decoded_bytes[base_dst + 3] = 255

            elif bytes_per_pixel == 4:
                # ARGB8888 (4 bytes per pixel)
                for i in range(total_pixels):
                    base = i * 4
                    decoded_bytes[base] = byte_data[base + 3]      # Blue
                    decoded_bytes[base + 1] = byte_data[base + 2]  # Green
                    decoded_bytes[base + 2] = byte_data[base + 1]  # Red
                    decoded_bytes[base + 3] = byte_data[base]      # Alpha
            else:
                return None

            q_img = QImage(decoded_bytes, width, height, QImage.Format_ARGB32)
            return QPixmap.fromImage(q_img)

        except Exception as e:
            print(f"Error parsing asset {filepath}: {e}")
            return None


