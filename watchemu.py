    def rebuild_dynamic_labels(self):
        """
        Dynamically instantiates and aligns QLabel components parsed from main.c.
        """
        for lbl in self.dyn_labels.values():
            lbl.deleteLater()
        self.dyn_labels.clear()

        labels_config = self.parsed_config.get("labels", [])
        if not labels_config:
            self.create_default_fallback_labels()
            return

        for l_conf in labels_config:
            parent_frame = self.get_parent_qframe(l_conf["parent_tile"])
            if not parent_frame:
                continue

            var_name = l_conf["var"]
            lbl = QLabel(parent_frame)

            w = l_conf["width"]
            if w == "auto":
                w = 410
            
            font_size = l_conf["font_size"]
            scale_factor = 1.0

            # Scale up big fonts to high-resolution assets to keep readable on desktop simulator
            if font_size >= 48:
                original_font_size = font_size
                font_size = 160  # Make it massive to fill up the screen
                scale_factor = font_size / original_font_size

            # Map proportionate height bounds using final font size to avoid vertical boundary truncation
            h = int(font_size * 1.15)
            
            align = l_conf["align"]
            dx = int(l_conf["x"] * scale_factor)
            dy = int(l_conf["y"] * scale_factor)

            x, y = self.calculate_coordinates(410, 502, w, h, align, dx, dy)
            lbl.setGeometry(x, y, w, h)
            lbl.setAlignment(Qt.AlignCenter)

            color_style = l_conf["color"]

            lbl.setStyleSheet(f"""
                QLabel {{
                    color: {color_style};
                    font-size: {font_size}px;
                    font-weight: bold;
                    background-color: transparent;
                    font-family: 'Segoe UI', Arial;
                }}
            """)
            lbl.show()
            self.dyn_labels[var_name] = lbl

    def rebuild_dynamic_launcher(self):
        """
        Dynamically renders launcher buttons based on parsed layout coordinates from main.c.
        """
        for btn in self.dyn_buttons:
            btn.deleteLater()
        self.dyn_buttons.clear()

        buttons_config = self.parsed_config.get("buttons", [])
        if not buttons_config:
            self.create_default_fallback_buttons()
            return

        for b_conf in buttons_config:
            # Only render buttons targeted to our launcher screen
            if b_conf.get("parent_tile") != "tile_launcher":
                continue

            btn = QPushButton(self.t_launcher)
            w = b_conf["w"]
            h = b_conf["h"]
            align = b_conf["align"]
            dx = b_conf["x"]
            dy = b_conf["y"]
            
            x, y = self.calculate_coordinates(410, 502, w, h, align, dx, dy)
            btn.setGeometry(x, y, w, h)
            
            # Form clean circles for circular watch icons
            r_border = w // 2
            btn.setStyleSheet(f"""
                QPushButton {{
                    background-color: black;
                    border: 2px solid #222;
                    border-radius: {r_border}px;
                }}
                QPushButton:hover {{
                    border-color: #00bfff;
                }}
            """)
            
            lbl = QLabel(btn)
            lbl.setGeometry(0, 0, w, h)
            lbl.setAlignment(Qt.AlignCenter)
            lbl.setStyleSheet("color: #a0a0a0; font-size: 14px; background-color: transparent;")
            
            img_src_key = b_conf.get("img_src")
            loaded_pixmap = None
            if img_src_key == "clock" and hasattr(self, 'pix_clock'):
                loaded_pixmap = self.pix_clock
            elif img_src_key == "gear" and hasattr(self, 'pix_gear'):
                loaded_pixmap = self.pix_gear

            if loaded_pixmap:
                # Scale parsed pixel-map to button container bounds with a 14px spacing margin
                scaled_pix = loaded_pixmap.scaled(w - 14, h - 14, Qt.KeepAspectRatio, Qt.SmoothTransformation)
                lbl.setPixmap(scaled_pix)
            else:
                # Fallback to readable label if asset parsing failed
                btn_name = b_conf["var"].replace("btn_", "").capitalize()
                lbl.setText(btn_name)

            # Bind active clicks to parsed layout slides
            target_var = b_conf.get("target_tile_var")
            if target_var in self.parsed_config["tiles"]:
                target_idx = self.parsed_config["tiles"][target_var]
                btn.clicked.connect(lambda _, idx=target_idx: self.tile_view.slide_to_tile(idx))

            btn.show()
            self.dyn_buttons.append(btn)

    def calculate_coordinates(self, parent_w, parent_h, w, h, align, dx, dy):
        """
        Translates standard LVGL align flags into standard PySide coordinates.
        """
        if align == "LV_ALIGN_TOP_LEFT":
            return dx, dy
        elif align == "LV_ALIGN_TOP_MID":
            return (parent_w - w) // 2 + dx, dy
        elif align == "LV_ALIGN_TOP_RIGHT":
            return parent_w - w + dx, dy
        elif align == "LV_ALIGN_BOTTOM_LEFT":
            return dx, parent_h - h + dy
        elif align == "LV_ALIGN_BOTTOM_MID":
            return (parent_w - w) // 2 + dx, parent_h - h + dy
        elif align == "LV_ALIGN_BOTTOM_RIGHT":
            return parent_w - w + dx, parent_h - h + dy
        else: # Default or LV_ALIGN_CENTER
            return (parent_w - w) // 2 + dx, (parent_h - h) // 2 + dy

    # Persist folder selection via JSON
    def save_workspace_path(self, folder):
        try:
            with open(CONFIG_FILE, 'w') as f:
                json.dump({"last_workspace": folder}, f, indent=4)
        except Exception as e:
            self.log_warn(f"Failed to save simulator configuration: {e}")

    def attempt_auto_load(self):
        """
        Autoloads the last folder workspace specified inside 'simulator_config.json'.
        """
        if os.path.exists(CONFIG_FILE):
            try:
                with open(CONFIG_FILE, 'r') as f:
                    data = json.load(f)
                    last_ws = data.get("last_workspace")
                    if last_ws and os.path.exists(last_ws):
                        self.load_workspace(last_ws)
                        return
            except Exception as e:
                self.log_warn(f"Failed to read simulator config: {e}")
        
        # Fallback to local scans if JSON config is missing
        base_dir = os.path.dirname(os.path.abspath(__file__))
        
        fallback_dirs = [
            os.path.join(base_dir, "Smartwatch_OS"),
            base_dir,
            os.path.join(base_dir, "..", "Smartwatch_OS")
        ]

        for d in fallback_dirs:
            if os.path.exists(d):
                # Search inside the directory to confirm it's actually the project
                for root, dirs, files in os.walk(d):
                    if "main.c" in files or "clock.c" in files:
                        self.load_workspace(d)
                        return

