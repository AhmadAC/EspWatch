    def system_refresh_loop(self):
        # Strip leading zeroes on hours to match the watch's display design
        hour_int = int(time.strftime("%H"))
        hour_str = str(hour_int)
        
        # Minutes display retains double-digit form
        min_str = time.strftime("%M")
        sec_str = time.strftime("%S")

        # Dynamically tick whatever labels were successfully discovered and parsed from main.c
        if "label_clock_hours" in self.dyn_labels:
            self.dyn_labels["label_clock_hours"].setText(hour_str)
        if "label_clock_minutes" in self.dyn_labels:
            self.dyn_labels["label_clock_minutes"].setText(min_str)
        if "label_clock_time" in self.dyn_labels:
            # Revert smoothly to original single-line ticker if user reverts code
            self.dyn_labels["label_clock_time"].setText(f"{hour_str}:{min_str}:{sec_str}")

        # Update telemetry labels dynamically
        if "lbl_battery" in self.dyn_labels:
            self.dyn_labels["lbl_battery"].setText(f"Bat: {self.battery_pct}%")
        if "lbl_wifi" in self.dyn_labels:
            self.dyn_labels["lbl_wifi"].setText("📶" if self.is_connected else "")

