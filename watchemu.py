# 3. TOUCH-SENSITIVE HORIZONTAL TILEVIEW CONTAINER
# ==============================================================================
class TouchTileView(QWidget):
    def __init__(self, parent=None):
        super().__init__(parent)
        self.setFixedSize(410, 502)
        
        self.container = QWidget(self)
        self.container.setGeometry(0, 0, 410 * 3, 502)
        
        self.dragging = False
        self.drag_start_x = 0
        self.container_start_x = 0
        self.current_tile = 1  # Start at Launcher (Tile 1)
        
        self.container.move(-410, 0)
        
        self.anim = QPropertyAnimation(self.container, b"pos")
        self.anim.setDuration(220)
        self.anim.setEasingCurve(QEasingCurve.OutQuad)

    def add_tile(self, widget, index):
        widget.setParent(self.container)
        widget.setGeometry(index * 410, 0, 410, 502)

    def mousePressEvent(self, event):
        if event.button() == Qt.LeftButton:
            self.anim.stop()
            self.dragging = True
            self.drag_start_x = event.globalPosition().x()
            self.container_start_x = self.container.x()
            event.accept()

    def mouseMoveEvent(self, event):
        if self.dragging:
            dx = event.globalPosition().x() - self.drag_start_x
            new_x = self.container_start_x + dx
            new_x = max(-820, min(0, new_x))
            self.container.move(new_x, 0)
            event.accept()

    def mouseReleaseEvent(self, event):
        if self.dragging:
            self.dragging = False
            target_tile = round(-self.container.x() / 410)
            self.slide_to_tile(target_tile)
            event.accept()

    def slide_to_tile(self, index):
        self.current_tile = index
        self.anim.setStartValue(self.container.pos())
        self.anim.setEndValue(QPoint(-index * 410, 0))
        self.anim.start()


